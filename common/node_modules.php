<?php

namespace Deployer;

task('deploy:previous_release:cleanup_node',  function () {
    if (!has('previous_release')) {
        return;
    }
    if (!test('[ -d {{previous_release}}/node_modules ]')) {
        return;
    }

    $sudo = get('cleanup_use_sudo') ? 'sudo' : '';
    run("$sudo rm -rf {{previous_release}}/node_modules");
});
